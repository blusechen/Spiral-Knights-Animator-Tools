package xandragon.model.math;

/**
 * Vector2 stored with floats.
 * @author Xan the Dragon
 */
public class Vector2f {
	
	/**The X component.*/
	public float x = 0;
	
	/**The Y component.*/
	public float y = 0;
	
	/**Create a blank Vector2f*/
	public Vector2f() {}
	
	/**Create a Vector3f*/
	public Vector2f(float _x, float _y) {
		x = _x;
		y = _y;
	}
}
